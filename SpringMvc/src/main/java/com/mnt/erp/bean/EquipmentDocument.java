package com.mnt.erp.bean;

/**
 * @author madhav
 *
 */
public class EquipmentDocument {
	
	private int equipmentDocumentId;
	private int equipmentId;
	private String documentName;
	private String documentPath;
	
	
	
	/**
	 * @return the equipmentDocumentId
	 */
	public int getEquipmentDocumentId() {
		return equipmentDocumentId;
	}
	/**
	 * @param equipmentDocumentId the equipmentDocumentId to set
	 */
	public void setEquipmentDocumentId(int equipmentDocumentId) {
		this.equipmentDocumentId = equipmentDocumentId;
	}
	/**
	 * @return the equipmentId
	 */
	public int getEquipmentId() {
		return equipmentId;
	}
	/**
	 * @param equipmentId the equipmentId to set
	 */
	public void setEquipmentId(int equipmentId) {
		this.equipmentId = equipmentId;
	}
	/**
	 * @return the documentName
	 */
	public String getDocumentName() {
		return documentName;
	}
	/**
	 * @param documentName the documentName to set
	 */
	public void setDocumentName(String documentName) {
		this.documentName = documentName;
	}
	/**
	 * @return the documentPath
	 */
	public String getDocumentPath() {
		return documentPath;
	}
	/**
	 * @param documentPath the documentPath to set
	 */
	public void setDocumentPath(String documentPath) {
		this.documentPath = documentPath;
	}
	
	
	

}
